if (Prev == "Moder") {
  beta <- log(c(0.13, 0.6, 1.6, 0.6, 1.5))
}
numbeta <- length(beta)

q <- 0.2 # Threshold for pcf
p <- 0.9 # Threshold for pnf
epsilon_beta <- abs(beta) /5
K = 1000 # Number of simulations

# --- Initialize Storage (统一版本) ---
# 【已修正】只保留 all_results_df 的初始化，删除所有 Res... 矩阵
beta_names <- paste0("beta_", 0:(numbeta - 1))
beta_se_names <- paste0(beta_names, "_se")
col_names <- c("Iteration", 
               "PCF", "PCF_se", 
               "PNF", "PNF_se", 
               "AUC", "AUC_se", 
               "IDI", "IDI_se", 
               "NRI_Conti", "NRI_Conti_se", 
               beta_names, beta_se_names)
all_results_df <- data.frame(matrix(NA, nrow = K, ncol = length(col_names)))
colnames(all_results_df) <- col_names

# --- Main Simulation Loop ---
for (k in 1:K)
{
  # 每次循环都使用 tryCatch 避免因单次错误中断整个模拟
  tryCatch({
    data.full <- Dat_gen(n, beta, Rho)
    fullFit_list <- Dat_fullFit(data.full)
    X <- cbind(rep(1, n), data.full$x1, data.full$x2, data.full$x3, data.full$z)
    
    pcf_list_full <- Dat_pcf_full(q, data.full, fullFit_list, X)
    pnf_list_full <- Dat_pnf_full(p, data.full, fullFit_list, X)
    auc_list_full <- Dat_auc_full(data.full, fullFit_list, X)
    idi_nri_list_full <- Dat_IDI_NRI_full(data.full, fullFit_list)
    
    # 【已修正】删除对 Res... 矩阵的赋值，只保留对 all_results_df 的赋值
    all_results_df[k, "Iteration"] <- k
    all_results_df[k, "PCF"] <- pcf_list_full$pcf
    all_results_df[k, "PCF_se"] <- sqrt(pcf_list_full$var_pcf)
    all_results_df[k, "PNF"] <- pnf_list_full$pnf
    all_results_df[k, "PNF_se"] <- sqrt(pnf_list_full$var_pnf)
    all_results_df[k, "AUC"] <- auc_list_full$auc
    all_results_df[k, "AUC_se"] <- sqrt(auc_list_full$var_auc)
    all_results_df[k, "IDI"] <- idi_nri_list_full$idi
    all_results_df[k, "IDI_se"] <- sqrt(idi_nri_list_full$var_idi)
    all_results_df[k, "NRI_Conti"] <- idi_nri_list_full$nri_conti
    all_results_df[k, "NRI_Conti_se"] <- sqrt(idi_nri_list_full$var_nri_conti)
    all_results_df[k, beta_names] <- fullFit_list$beta.full
    all_results_df[k, beta_se_names] <- sqrt(fullFit_list$beta.var.full)
    
    if (round(k / 10) == k / 10)
    {
      print(k)
    }
  }, error = function(e) {
    # 增加错误处理，打印错误信息但不停下整个模拟
    cat("Error in iteration", k, ":", e$message, "\n")
  })
}

