Dat_pcf_full <- function(q, data, fullFit_list, X){
  #q: threshold for calculating pcf
  #fullFit_list: generated by Dat_fullFit(data)
  h1 <- fullFit_list$h1
  h0 <- fullFit_list$h0
  yhat.full <- fullFit_list$yhat.full
  beta.full <- fullFit_list$beta.full
  
  #calculate pcf(q)
  quantile.full <- quantile(yhat.full, probs=c(0,1-q,1))
  
  p0_hat <- quantile.full[2]
  #sum(yhat.full==p0_hat)
  #num_pcf <- sum(yhat.full[yhat.full>=p0_hat])
  yhat.full.sort <- sort(yhat.full)
  num_pcf <- sum(yhat.full.sort[(round(n*(1-q))):n])
  denom_pcf <- sum(yhat.full)
  pcf <- num_pcf/denom_pcf
  
  ## numerical method for dPCF/dbeta
  dT_beta<- numeric(numbeta)
  yfit1 <- numeric(nrow(data))
  yfit2 <- numeric(nrow(data))
  for (i in 1:numbeta){
    tmp1 <- beta.full
    tmp2 <- beta.full
    tmp1[i] <- beta.full[i] + epsilon_beta[i]
    tmp2[i] <- beta.full[i] - epsilon_beta[i]
    yfit1 <- exp(X %*% tmp1)/(1 + exp(X %*% tmp1))
    yfit2 <-  exp(X %*% tmp2)/(1 + exp(X %*% tmp2))
    #quantile1 <- quantile(yfit1, probs=c(0,1-q,1))
    #quantile2 <- quantile(yfit2, probs=c(0,1-q,1))
    #p01 <- quantile1[2]
    #p02 <- quantile2[2]
    
    #num1 <- sum(yfit1[yfit1>=p01])
    #num2 <- sum(yfit2[yfit2>=p02])
    yfit1.sort <- sort(yfit1)
    yfit2.sort <- sort(yfit2)
    num1 <- sum(yfit1.sort[(round(n*(1-q))):n])
    num2 <- sum(yfit2.sort[(round(n*(1-q))):n])
    denom1 <- sum(yfit1)
    denom2 <- sum(yfit2)
    dT_beta[i] <- (num1/denom1-num2/denom2)/(2*epsilon_beta[i])
  }
  
  #Calculate influence function for T(Risk distribution at F_inverse(1-q))
  phi <- (p0_hat*(1-q)-yhat.full*(1-pcf)-(p0_hat-yhat.full)*as.numeric(yhat.full<=p0_hat))/(denom_pcf/n)
  
  # calculate influence function for PCF at p0_hat
  h1_pcf <- dT_beta %*% h1 - phi
  h0_pcf <- dT_beta %*% h0 - phi
  
  emp_cov_pcf <- sum((h1_pcf)^2*data$y, (h0_pcf)^2*(1-data$y))
  var_pcf <- emp_cov_pcf/n^2
  return(list(pcf=pcf, var_pcf=var_pcf))
}

#1.2 Calculate pnf by fitting the logistic regression model
Dat_pnf_full <- function(p, data, fullFit_list, X){
  #p: threshold for calculating pnf
  #fullFit_list: generated by Dat_fullFit(data)
  h1 <- fullFit_list$h1
  h0 <- fullFit_list$h0
  yhat.full <- fullFit_list$yhat.full
  beta.full <- fullFit_list$beta.full
  
  #calculate pnf(p)
  denom.full <- sum(yhat.full)
  yhat.full.sort <- sort(yhat.full, decreasing=T)
  num.full <- cumsum(yhat.full.sort)
  ratio.full <- num.full/denom.full
  indices <- which((ratio.full<=p+0.05) & (ratio.full>=p))
  r_hat <- max(yhat.full.sort[indices])
  
  #pnf <- sum(yhat.full>=r_hat)/n
  pnf <- as.numeric(indices[1]/n)
  ## numerical method for dPNF/dbeta
  dT_beta<- numeric(numbeta)
  yfit1 <- numeric(nrow(data))
  yfit2 <- numeric(nrow(data))
  for (i in 1:numbeta){
    tmp1 <- beta.full
    tmp2 <- beta.full
    tmp1[i] <- beta.full[i] + epsilon_beta[i]
    tmp2[i] <- beta.full[i] - epsilon_beta[i]
    yfit1 <- exp(X %*% tmp1)/(1 + exp(X %*% tmp1))
    yfit2 <-  exp(X %*% tmp2)/(1 + exp(X %*% tmp2))
    
    denom1 <- sum(yfit1)
    denom2 <- sum(yfit2)
    yfit1.sort <- sort(yfit1, decreasing=T)
    yfit2.sort <- sort(yfit2, decreasing=T)
    num1 <- cumsum(yfit1.sort)
    num2 <- cumsum(yfit2.sort)
    ratio1 <- num1/denom1
    ratio2 <- num2/denom2
    indices1 <- which((ratio1<=p+0.05) & (ratio1>=p))
    indices2 <- which((ratio2<=p+0.05) & (ratio2>=p))
    r1 <- max(yfit1.sort[indices1])
    r2 <- max(yfit2.sort[indices2])
    
    #pnf1 <- sum(yfit1>=r1)/n
    #pnf2 <- sum(yfit2>=r2)/n
    pnf1 <- as.numeric(indices1[1]/n)
    pnf2 <- as.numeric(indices2[1]/n)
    dT_beta[i] <- (pnf1-pnf2)/(2*epsilon_beta[i])
  }
  
  #Calculate influence function for T(Risk distribution at G_inverse(1-p))
  phi <- as.numeric(yhat.full<=r_hat) + yhat.full/r_hat*((1-p)-as.numeric(yhat.full<=r_hat))-(1-pnf)
  
  # calculate influence function for PCF at p0_hat
  h1_pnf <- dT_beta %*% h1 - phi
  h0_pnf <- dT_beta %*% h0 - phi
  
  emp_cov_pnf <- sum((h1_pnf)^2*data$y, (h0_pnf)^2*(1-data$y))
  var_pnf <- emp_cov_pnf/n^2
  return(list(pnf=pnf, var_pnf=var_pnf))
}

#1.3 Calculate auc by fitting the logistic regression model
Dat_auc_full <- function(data, fullFit_list, X){
  #fullFit_list: generated by Dat_fullFit(data)
  h1 <- fullFit_list$h1
  h0 <- fullFit_list$h0
  yhat.full <- fullFit_list$yhat.full
  beta.full <- fullFit_list$beta.full
  
  #calculate auc
  #c <- seq(0, 1, by=0.05) #possible threshold values for ROC curve
  c <- c(seq(0, 1, by=0.01)*max(yhat.full),1)
  denom_tpr.full <- sum(yhat.full)
  denom_fpr.full <- sum(1-yhat.full)
  num_tpr.full <- numeric(length(c))
  num_fpr.full <- numeric(length(c))
  for (j in 1:length(c)){
    ind <- (yhat.full>=c[j])
    num_tpr.full[j] <- sum(yhat.full[ind==1])
    num_fpr.full[j] <- sum((1-yhat.full)[ind==1])
  }
  tpr.full <- num_tpr.full/denom_tpr.full
  fpr.full <- num_fpr.full/denom_fpr.full
  auc <- auc(fpr.full, tpr.full)
  
  # numerical method for dFPR/dbeta and dTPR/dbeta as a function of c
  dFPR_beta <- matrix(0, nrow=numbeta, ncol=length(c))
  dTPR_beta <- matrix(0, nrow=numbeta, ncol=length(c))
  yfit1 <- numeric(nrow(data))
  yfit2 <- numeric(nrow(data))
  for (i in 1:numbeta){
    tmp1 <- beta.full
    tmp2 <- beta.full
    tmp1[i] <- beta.full[i] + epsilon_beta[i]
    tmp2[i] <- beta.full[i] - epsilon_beta[i]
    yfit1 <- exp(X %*% tmp1)/(1 + exp(X %*% tmp1))
    yfit2 <-  exp(X %*% tmp2)/(1 + exp(X %*% tmp2))
    num1_tpr <- 0
    num2_tpr <- 0
    num1_fpr <- 0
    num2_fpr <- 0
    denom1_tpr <- sum(yfit1)
    denom2_tpr <- sum(yfit2)
    denom1_fpr <- sum(1-yfit1)
    denom2_fpr <- sum(1-yfit2)
    
    for (j in 1:length(c)){
      ind1 <- (yfit1>=c[j])
      ind2 <- (yfit2>=c[j])
      num1_tpr <- sum(yfit1[ind1==1])
      num2_tpr <- sum(yfit2[ind2==1])
      num1_fpr <- sum((1-yfit1)[ind1==1])
      num2_fpr <- sum((1-yfit2)[ind2==1])
      dTPR_beta[i,j] <- (num1_tpr/denom1_tpr-num2_tpr/denom2_tpr)/(2*epsilon_beta[i])
      dFPR_beta[i,j] <- (num1_fpr/denom1_fpr-num2_fpr/denom2_fpr)/(2*epsilon_beta[i])
    }
  }
  
  # calculate influence function for TPR/FPR as a function of c
  mat_tpr.full <- matrix(0, nrow=nrow(data), ncol=length(c))
  mat_fpr.full <- matrix(0, nrow=nrow(data), ncol=length(c))
  for (j in 1:length(c)){
    ind <- (yhat.full>=c[j])
    mat_tpr.full[,j] <- yhat.full*(ind-tpr.full[j])/(denom_tpr.full/n)
    mat_fpr.full[,j] <- (1-yhat.full)*(ind-fpr.full[j])/(denom_fpr.full/n)
  }
  
  # calculate the influence function for tpr/fpr
  h1_tpr <- t(h1) %*% dTPR_beta + mat_tpr.full
  h0_tpr <- t(h0) %*% dTPR_beta + mat_tpr.full
  h1_fpr <- t(h1) %*% dFPR_beta + mat_fpr.full
  h0_fpr <- t(h0) %*% dFPR_beta + mat_fpr.full
  
  #calcualte influence function for auc
  h1_auc <- 0-apply(h1_fpr, 1, function(x) auc(tpr.full, x))+apply(h1_tpr, 1, function(x) auc(fpr.full, x))
  h0_auc <- 0-apply(h0_fpr, 1, function(x) auc(tpr.full, x))+apply(h0_tpr, 1, function(x) auc(fpr.full, x))
  
  emp_cov_auc <- sum((h1_auc)^2*data$y, (h0_auc)^2*(1-data$y))
  var_auc <- emp_cov_auc/n^2
  return(list(auc=auc, var_auc=var_auc))
}

# Function to calculate IDI, continuous NRI, and their variances
Dat_IDI_NRI_full <- function(data, fullFit_list){
  
  # --- 1. 定义新模型(m1)和基准模型(m0)的预测值 ---
  
  # 新模型的预测值 yhat1 来自已经计算好的 fullFit_list
  yhat1 <- fullFit_list$yhat.full
  
  # 拟合基准模型 m0 (不包含z)，并得到其预测值 yhat0
  m0 <- glm(y ~ x1 + x2 + x3, family=binomial, data=data)
  yhat0 <- m0$fitted.values
  
  # --- 2. 计算IDI及其方差 ---
  
  # 计算新旧模型预测概率的差值
  delta_p <- yhat1 - yhat0
  delta_p_case <- delta_p[data$y == 1]
  delta_p_con <- delta_p[data$y == 0]
  
  # 计算 IDI
  idi_value <- mean(delta_p_case) - mean(delta_p_con)
  
  # 计算 IDI 的方差
  var_delta_p_case <- var(delta_p_case) / length(delta_p_case)
  var_delta_p_con <- var(delta_p_con) / length(delta_p_con)
  var_idi <- var_delta_p_case + var_delta_p_con
  
  # --- 3. 计算连续型NRI及其方差  ---
  
  # 计算 P(up|case) 和 P(up|control)
  # Hmisc::improveProb 也可以计算，但为了与方差计算逻辑统一，我们手动计算
  p_up_case <- mean(delta_p_case > 0)
  p_up_control <- mean(delta_p_con > 0)
  nri_conti_value <- 2 * (p_up_case - p_up_control)
  
  # 创建指示变量 (indicator) 用于计算NRI方差
  indicator_case <- ifelse(delta_p_case > 0, 1, 0)
  indicator_con <- ifelse(delta_p_con > 0, 1, 0)
  
  # 计算 NRI 的方差
  var_indicator_case <- var(indicator_case) / length(indicator_case)
  var_indicator_con <- var(indicator_con) / length(indicator_con)
  # 关键：记住乘以4，因为NRI的定义里有系数2
  var_nri_conti <- 4 * (var_indicator_case + var_indicator_con)
  
  # --- 4. 返回所有计算结果 ---
  
  return(list(
    idi = idi_value, 
    var_idi = var_idi, 
    nri_conti = nri_conti_value, 
    var_nri_conti = var_nri_conti
  ))
}







# --- Simulation Setup ---
set.seed(0)
Prev <- "Rare"
# 【已修正】将 Rho 改为正确的字符串格式
Rho <- 0.5#"Independent"
